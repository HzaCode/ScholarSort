function getJournalData(journalName) {
    if (!journalName) return null;
    
    const cleanName = journalName.toLowerCase()
        .replace(/[^\w\s-]/g, ' ')
        .replace(/\s+/g, ' ')
        .trim();
    
    const data = JOURNAL_IMPACT_FACTORS[cleanName];
    
    // If it's a string (alias), follow the reference
    if (typeof data === 'string') {
        return JOURNAL_IMPACT_FACTORS[data];
    }
    
    // If it's an object (main entry), return it
    if (typeof data === 'object') {
        return data;
    }
    
    return null;
}

